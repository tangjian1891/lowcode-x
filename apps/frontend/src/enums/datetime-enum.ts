// 日期时间 归属于elemen-plus的 el-date-picker组件
export enum DateTimeEnum {
  "YYYY" = "YYYY", //年
  "YYYY_MM" = "YYYY-MM", //年月
  "YYYY_MM_DD" = "YYYY-MM-DD", //年月日
  "YYYY_MM_DD_HH" = "YYYY-MM-DD HH", //年月日时
  "YYYY_MM_DD_HH_mm" = "YYYY-MM-DD HH:mm", //年月日时分
  "YYYY_MM_DD_HH_mm_ss" = "YYYY-MM-DD HH:mm:ss", //年月日时分秒
  "HH" = "HH", //时
  "HH__mm" = "HH:mm", //时分
  "HH__mm_ss" = "HH:mm:ss", //时分秒
}
